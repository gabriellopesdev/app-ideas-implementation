export function addValue(currentValue: string, valueToOperate: string) {
    return Number(currentValue) + Number(valueToOperate)    
}

export function subValue(currentValue: string, valueToOperate: string) {
    return Number(currentValue) - Number(valueToOperate)    
}

export function divValue(currentValue: string, valueToOperate: string) {
    return Number(currentValue) / Number(valueToOperate)    
}

export function multiplyValue(currentValue: string, valueToOperate: string) {
    return Number(currentValue) * Number(valueToOperate)    
}